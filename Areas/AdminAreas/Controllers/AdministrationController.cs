        [HttpGet]
        public IActionResult ListUsers(string searchString)
        {
            var filters = userManager.Users.ToList();


            var filter = new List<ApplicationUser>();

            if (searchString != null)
            {
                foreach (var f in filters)
                {
                    if (f.Email.ToLower().Contains(searchString.ToLower()))
                    {
                        filter.Add(f);
                    }
                }
            }
            else
            {
                filter = filters;
            }
            return View(filter);

        }



        [HttpGet]
        public async Task<IActionResult> EditUser(string id)
        {
            var user = await userManager.FindByIdAsync(id);

            if (user == null)
            {
                ViewBag.ErroreMessage = $"User with ID = {id} cannot be found";
                return View("NotFound");
            }

            var userClaims = await userManager.GetClaimsAsync(user);
            var userRoles = await userManager.GetRolesAsync(user);


            var model = new EditUserViewModel
            {
                Id = user.Id,
                Email = user.Email,
                UserName = user.UserName,
                Claims = userClaims.Select(c => c.Value).ToList(),
                Roles = (List<string>)userRoles
            };

            return View(model);
        }


